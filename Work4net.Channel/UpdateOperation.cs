namespace Work4net.Channel
{
    /// <summary>
    /// The update operation types
    /// </summary>
    public enum UpdateOperation
    {
        /// <summary>
        /// The object gets added to scheduler
        /// </summary>
        ADD,

        /// <summary>
        /// The object is updated
        /// </summary>
        UPDATE,

        /// <summary>
        /// Remove object from scheduler
        /// </summary>
        REMOVE
    }
}